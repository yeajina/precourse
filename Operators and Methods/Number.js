// 2. Subtraction (-)
function getDifference(num1, num2); {
    var result = num1 - num2;
    return result;
}

var resultDiff = getDifference(30, 10);
console.log('should be 20:', resultDiff);

//example-operator(-)
var difference = 12 - 4;
